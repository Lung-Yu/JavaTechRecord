package DynamicSettingPrivate;

public class Student {
	private int id;
	private String name;
	private boolean male;
	private double account;
	
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public boolean isMale() {
		return male;
	}

	public double getAccount() {
		return account;
	}

}
